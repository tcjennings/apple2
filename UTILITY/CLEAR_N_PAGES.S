*
* CLEAR N PAGES
* REQUIRES 65C02
*
* CLEARS X PAGES OF MEMORY BASED ON
* STARTING ADDRESS IN FE.FF
* MEMORY IS OVERWRITTEN WITH A
*
 ORG $300
 LDX #$4 ; pages to clear
 LDA #$8 ; starting page
 STZ $FE
 STA $FF
 LDA #$0 ; clear byte (NULL)
NEXTPAGE
 LDY #$0
LOOP
 STA ($FE),Y
 INY
 BNE LOOP
 INC $FF
 DEX
 BNE NEXTPAGE
 RTS

